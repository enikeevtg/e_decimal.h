Suite* add_func(void) {
  Suite* s;
  TCase* tc_mul;

  s = suite_create("mul function");

  tc_mul = tcase_create("two numbers multiplication");
  tcase_add_test(tc_mul, mul);
  // tcase_add_test(tc_add, add_pos_pos);
  // tcase_add_test(tc_add, add_neg_pos);
  // tcase_add_test(tc_add, add_pos_neg);
  // tcase_add_test(tc_add, add_neg_neg);
  suite_add_tcase(s, tc_mul);

  return s;
}

int main(void) {
  Suite* mul_suite;
  SRunner* runner;

  mul_suite = add_func();
  runner = srunner_create(mul_suite);

  srunner_run_all(runner, CK_NORMAL);
  int tests_count = srunner_ntests_run(runner);
  int failed = srunner_ntests_failed(runner);
  srunner_free(runner);

  printf("\033[0;32m  SUCCESS: %d\n", tests_count - failed);
  printf("\033[0;31m  FAILED: %d\n", failed);

  return failed ? 1 : 0;
}
